public class ItemServices {

    private String serviceId;
    private String serviceCategory;
    private String serviceName;
    private String serviceProjects;
    private String serviceVerified;
    private String serviceImage;


    public String getServiceId() {
        return serviceId;
    }

    public void setServiceId(String serviceId) {
        this.serviceId = serviceId;
    }

    public String getServiceCategory() {
        return serviceCategory;
    }

    public void setServiceCategory(String serviceCategory) {
        this.serviceCategory = serviceCategory;
    }

    public String getServiceName() {
        return serviceName;
    }

    public void setServiceName(String serviceName) {
        this.serviceName = serviceName;
    }

    public String getServiceProjects() {
        return serviceProjects;
    }

    public void setServiceProjects(String serviceProjects) {
        this.serviceProjects = serviceProjects;
    }

    public String getServiceVerified() {
        return serviceVerified;
    }

    public void setServiceVerified(String serviceVerified) {
        this.serviceVerified = serviceVerified;
    }

    public String getServiceImage() {
        return serviceImage;
    }

    public void setServiceImage(String serviceImage) {
        this.serviceImage = serviceImage;
    }
}
